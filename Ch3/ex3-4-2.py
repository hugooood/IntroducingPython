#使用dict()转换为字典

#双列表创建字典
# lol (a list of two-item list)
lol = [['a','b'],['c','d'],['e','f']]
print(dict(lol))

#可以对任何包含双值子序列的序列使用dict()

#包含双值列表的元组
# tol (a tuple of two-item list)
tol = [('a','b'),('c','d'),('e','f')]
print(dict(lot))   #转换

#双字符的字符串组成的列表
los = ['ab','cd','ef']
